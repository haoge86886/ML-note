#正则化
#在损失函数中加入“限制项”，惩罚模型过于复杂的行为，从而提升模型在新数据上的泛化能力。
#w是参数,J(w,b) = 1/2m ∑ (f(xi) - yi)² + λ/2m ∑ wj²
#λ是正则化参数,λ/2m ∑ wj^2是正则化项

#正则化参数
#λ太小,惩罚不够,过拟合
#λ太大,惩罚太大,欠拟合

#线性回归正则化
#J(w,b) = 1/2m ∑ (f(xi) - yi)² + λ/2m  ∑ wj²
#wj = wj - α/m ∑ (f(xi)-yi)*xi  + α*λ/m*wj
#   = wj(1-αλ/m) - α/m ∑ (f(xi) - yi) * xi
#b = b - α * 1/m ∑ (f(xi) - yi)

#逻辑回归正则化
#J(w,b) = -1/m Σ [yi*ln(f(xi)) + (1-yi)ln(1-f(xi))] + λ/2m Σ wj²
#wj = wj - α/m ∑ (f(xi)-yi)*xi  + α*λ/m*wj
#b = b - α * 1/m ∑ (f(xi) - yi)